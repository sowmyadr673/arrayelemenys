package com.xworkz.arrayelements;

public class CollegeBlocks {

	public static void main(String[] args) {

		char collegeblocks[] = { 'A', 'B', 'C', 'D' };
		System.out.println("size of the array" + collegeblocks.length);
		System.out.println("college blocks are:");
		for (int i = 0; i < collegeblocks.length; i++) {
			System.out.println(collegeblocks[i]);
		}

	}
}